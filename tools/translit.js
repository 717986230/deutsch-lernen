function one(word) {
  let s = word.toLowerCase().replace(/[^a-zäöüß]/g, ''), out = '', i = 0
  outer: while (i < s.length) {
    for (const [k, v] of MAP) {
      if (s.startsWith(k, i)) { out += v; i += k.length; continue outer }
    }
    i++
  }
  return out
}
function transliterate(de) {
  const m = de.match(/^(der|die|das)\s+(.+)$/)
  if (m) return ART[m[1]] + ' ' + one(m[2])
  // 多词短语：逐词
  if (de.includes(' ')) return de.split(/\s+/).map(one).join(' ')
  return one(de)
}
